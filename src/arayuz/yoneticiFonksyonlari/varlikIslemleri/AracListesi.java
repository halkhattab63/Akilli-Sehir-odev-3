    private void aracListMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_aracListMouseClicked
        // TODO add your handling code here:
        int selectedRow = aracList.getSelectedRow();
        if (selectedRow != -1) {
            aIsim.setText(aracListesiModel.getValueAt(selectedRow, 0).toString());
            aKonum.setSelectedItem(aracListesiModel.getValueAt(selectedRow, 1).toString());
            aKapasite.setText(aracListesiModel.getValueAt(selectedRow, 2).toString());
            aTuketim.setText(aracListesiModel.getValueAt(selectedRow, 3).toString());
            
            Arac arac = araclar.get(selectedRow);
            if(arac instanceof ElektrikliArac elektrikliArac){
                sarj.setText(String.valueOf("%" + elektrikliArac.getSarj()));
                elekArac.setVisible(true);
                if(elektrikliArac.getSarj() != 100)
                    istasyon.setVisible(true);
                else
                    istasyon.setVisible(false);
                    
            }
            else
                elekArac.setVisible(false);
        }
    }//GEN-LAST:event_aracListMouseClicked

    private void aSilActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_aSilActionPerformed
        // TODO add your handling code here:
        int selectedRow = aracList.getSelectedRow();
        if (selectedRow != -1) {
            int konum = Integer.parseInt(aKonum.getSelectedItem().toString());
            TrafikYonetimi.bolgeler.put(konum, TrafikYonetimi.bolgeler.get(konum) - 1);
            araclar.remove(selectedRow);
            aracListesiModel.removeRow(selectedRow);
            JOptionPane.showMessageDialog(this, "Araç başarıyla silinmiştir!", "Başarılı Işlem", JOptionPane.ERROR_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(this, "Lütfen bir satır seçin!", "Hata", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_aSilActionPerformed

    private void istasyonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_istasyonActionPerformed
        // TODO add your handling code here:
        int selectedRow = aracList.getSelectedRow();
        ElektrikliArac elektrikliArac = (ElektrikliArac) araclar.get(selectedRow);
        elektrikliArac.setSarj(100);
        istasyon.setVisible(false);
        sarj.setText(String.valueOf("%" + elektrikliArac.getSarj()));
        JOptionPane.showMessageDialog(this, "Aracın şarjı dolmuştur!", "Şarj", JOptionPane.INFORMATION_MESSAGE);
        
    }//GEN-LAST:event_istasyonActionPerformed

