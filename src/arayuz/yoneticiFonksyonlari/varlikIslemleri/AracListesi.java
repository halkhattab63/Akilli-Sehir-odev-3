    private void aGunActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_aGunActionPerformed
        // TODO add your handling code here:
        int selectedRow = aracList.getSelectedRow();
        if (selectedRow != -1) {
            try{
                Arac arac = null;
                String isim = aIsim.getText();
                int konum =  Integer.parseInt(aKonum.getSelectedItem().toString());
                int kapasite =  Integer.parseInt(aKapasite.getText());
                float tuketim =  Float.parseFloat(aTuketim.getText());

                if (isim == null || isim.trim().isEmpty())
                    throw new IllegalArgumentException("Araç ismi boş olamaz!"); // Bina ismi eksik veya yanlış girilmiş

                if (kapasite <= 0) 
                    throw new IllegalArgumentException("Kapasite 0 veya negatif olamaz!"); // Kapasite hatalı

                if (tuketim < 0) 
                    throw new IllegalArgumentException("Araç tüketimi negatif olamaz!"); // Tüketim değerleri doğru değil
                
                switch ((String) aracListesiModel.getValueAt(selectedRow,4)) {
                    case "Otobüs" -> arac = new Otobus(isim, konum, kapasite, tuketim);
                    case "Otomobil" -> arac = new Otomobil(isim, konum, kapasite, tuketim);
                    case "Motosiklet" -> arac = new Motosiklet(isim, konum, kapasite, tuketim);
                    case "Elektrikli araç" -> arac = new ElektrikliArac(isim, konum, kapasite, tuketim);
                    default -> throw new IllegalArgumentException("Araç türü geçersiz!"); // Geçersiz bina türü hatası
                }
                int eskiKonum = Integer.parseInt((String) aKonum.getSelectedItem());  
                int yeniKonum = Integer.parseInt(aracListesiModel.getValueAt(selectedRow, 1).toString());
                TrafikYonetimi.bolgeler.put(eskiKonum, TrafikYonetimi.bolgeler.get(eskiKonum) + 1);
                TrafikYonetimi.bolgeler.put(yeniKonum, TrafikYonetimi.bolgeler.get(yeniKonum) - 1);
                araclar.remove(araclar.size() - 1);
                araclar.set(selectedRow, arac);
                JOptionPane.showMessageDialog(this, "Araç başarıyla güncellendi", "Başarılı Işlem", JOptionPane.INFORMATION_MESSAGE);
            } catch (NumberFormatException e) {
                // Sayısal bir değer beklenirken geçersiz bir giriş yapıldığında
                JOptionPane.showMessageDialog(this, "Lütfen geçerli bir sayı girin!", "Hata", JOptionPane.ERROR_MESSAGE);
            } catch (IllegalArgumentException e) {
                // Geçersiz bina türü gibi durumlarda
                JOptionPane.showMessageDialog(this, e.getMessage(), "Hata", JOptionPane.ERROR_MESSAGE);
            } catch (Exception e) {
                // Diğer beklenmeyen hatalar
                JOptionPane.showMessageDialog(this, "Bilinmeyen bir hata oluştu", "Hata", JOptionPane.ERROR_MESSAGE);
            }
                
                aracListesiModel.setRowCount(0); // Clear the table

                for (Arac e : araclar) {
                    aracListesiModel.addRow(new Object[] {
                        e.getIsim(),
                        e.getKonum(),
                        e.getKapasite(),
                        e.getTuketim(),
                        e.getTip()
                    });
                }
            } else {
            JOptionPane.showMessageDialog(this, "Lütfen bir satır seçin!", "Hata", JOptionPane.ERROR_MESSAGE);
        }
        
    }//GEN-LAST:event_aGunActionPerformed

