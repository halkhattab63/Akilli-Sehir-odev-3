    private void eGunActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_eGunActionPerformed
        // TODO add your handling code here:
        int selectedRow = kaynakList.getSelectedRow();
        if (selectedRow != -1) {
            try {
                Enerjikaynagi kaynak = null;
                String isim = eIsim1.getText();
                int konum =  Integer.parseInt(eKonum1.getSelectedItem().toString());
                int kapasite =  Integer.parseInt(eKapasite1.getText());
                float ure =  Float.parseFloat(uretim1.getText());

                // Veri girişlerinde boş ya da hatalı veri kontrolü
                if (isim == null || isim.trim().isEmpty())
                    throw new IllegalArgumentException("Kaynak ismi boş olamaz!"); // Bina ismi eksik veya yanlış girilmiş

                if (kapasite <= 0) 
                    throw new IllegalArgumentException("Kapasite 0 veya negatif olamaz!"); // Kapasite hatalı

                if (ure < 0) 
                    throw new IllegalArgumentException("Enerji üretimi negatif olamaz!"); // Tüketim değerleri doğru değil
                
                switch ((String) kaynakListesiModel.getValueAt(selectedRow, 4)) {
                    case "Güneş" -> kaynak = new Gunes(isim, konum, kapasite, ure);
                    case "Rüzgar" -> kaynak = new Ruzgar(isim, konum, kapasite, ure);
                    case "Fosil" -> kaynak = new Fosil(isim, konum, kapasite, ure);
                    case "Nükleer" -> kaynak = new Nukleer(isim, konum, kapasite, ure);
                    default -> throw new IllegalArgumentException("Kaynak türü geçersiz!"); // Geçersiz bina türü hatası
                }
                
                enerjikaynaklari.remove(enerjikaynaklari.size() - 1);
                enerjikaynaklari.set(selectedRow, kaynak);
                
                kaynakListesiModel.setRowCount(0); // Clear the table

                for (Enerjikaynagi e : enerjikaynaklari) {
                    kaynakListesiModel.addRow(new Object[] {
                        e.getIsim(),
                        e.getKonum(),
                        e.getKapasite(),
                        e.getUretim(),
                        e.getTip()
                    });
                }
                JOptionPane.showMessageDialog(this, "Kaynak başarıyla güncellendi", "Başarılı Işlem", JOptionPane.INFORMATION_MESSAGE);
            } catch (NumberFormatException e) {
                // Sayısal bir değer beklenirken geçersiz bir giriş yapıldığında
                JOptionPane.showMessageDialog(this, "Lütfen geçerli bir sayı girin!", "Hata", JOptionPane.ERROR_MESSAGE);
            } catch (IllegalArgumentException e) {
                // Geçersiz bina türü gibi durumlarda
                JOptionPane.showMessageDialog(this, e.getMessage(), "Hata", JOptionPane.ERROR_MESSAGE);
            } catch (Exception e) {
                // Diğer beklenmeyen hatalar
                JOptionPane.showMessageDialog(this, "Bilinmeyen bir hata oluştu: ", "Hata", JOptionPane.ERROR_MESSAGE);
            }
            } else {
            JOptionPane.showMessageDialog(this, "Lütfen bir satır seçin!", "Hata", JOptionPane.ERROR_MESSAGE);
        }
        

    }//GEN-LAST:event_eGunActionPerformed

