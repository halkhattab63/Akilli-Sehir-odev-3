    private void kaynakListMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_kaynakListMouseClicked
        // TODO add your handling code here:
        int selectedRow = kaynakList.getSelectedRow();
        if (selectedRow != -1) {
            eIsim1.setText(kaynakListesiModel.getValueAt(selectedRow, 0).toString());
            eKonum1.setSelectedItem(kaynakListesiModel.getValueAt(selectedRow, 1).toString());
            eKapasite1.setText(kaynakListesiModel.getValueAt(selectedRow, 2).toString());
            uretim1.setText(kaynakListesiModel.getValueAt(selectedRow, 3).toString());
        }
    }//GEN-LAST:event_kaynakListMouseClicked

    private void eSilActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_eSilActionPerformed
        // TODO add your handling code here:
        int selectedRow = kaynakList.getSelectedRow();
        if (selectedRow != -1) {       
            enerjikaynaklari.remove(selectedRow);
            kaynakListesiModel.removeRow(selectedRow);
            JOptionPane.showMessageDialog(this, "Kayanak başarıyala silindi!", "Başarılı Işlem", JOptionPane.INFORMATION_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(this, "Lütfen bir satır seçin!", "Hata", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_eSilActionPerformed

